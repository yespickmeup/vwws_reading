    List<Meter_readers.to_meter_readers> meter_readers = new ArrayList();
    List<Meter_reader_assignments.to_meter_reader_assignments> meter_reader_assignments = new ArrayList();
    List<Readings.to_readings> readings = new ArrayList();
    List<Occupancy_types.to_occupancy_types> occupancy_types = new ArrayList();

    private void ret_readings() {
        String where = " ";
        String where2 = " order by created_by desc limit 6000 ";
        meter_readers = Meter_readers.download_from_server(where);
        meter_reader_assignments = Meter_reader_assignments.ret_data_server(where);
        readings = Readings.ret_data_server(where2);
        occupancy_types = Occupancy_types.ret_data_server(where);
        jLabel11.setText("" + meter_readers.size());
        jLabel14.setText("" + meter_reader_assignments.size());
        jLabel16.setText("" + readings.size());
        jLabel18.setText("" + occupancy_types.size());
    }

    private void upload_readings() {
        jProgressBar1.setString("Downloading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                ret_readings();
                jButton1.setText("Save");
                jButton1.setEnabled(true);
                jButton2.setEnabled(true);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();
    }

    private void save_settings() {
        jProgressBar1.setString("Saving...Please wait...");
        jProgressBar1.setIndeterminate(true);
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                Meter_readers.add_downloaded(meter_readers);
                Meter_reader_assignments.add_downloaded(meter_reader_assignments);
                Readings.add_data_downloaded(readings);
                Occupancy_types.add_data_downloaded(occupancy_types);
                Alert.set(1, "");
                jButton1.setText("Download");
                jButton1.setEnabled(true);
                jButton2.setEnabled(true);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();
    }
