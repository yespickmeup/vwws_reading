    List<Readings.to_readings> readings = new ArrayList();

    private void ret_readings() {
        String where = " where is_uploaded=0 and meter_reader_id='" + MyUser.getUser_id() + "' ";
        readings = Readings.ret_data(where);
        jLabel11.setText("" + readings.size());

    }

    private void upload_readings() {
        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        jButton1.setEnabled(false);
        jButton2.setEnabled(false);
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {

                Readings.upload_to_server(readings);
                Alert.set(0, "Successfully Uploaded!");
                ret_readings();
                jButton1.setEnabled(true);
                jButton2.setEnabled(true);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();
    }
