    private void login() {
        String username = jTextField1.getText();
        String password = jPasswordField1.getText();
        password = DeEncrypter.encrypt(password);

        String where = " where user_name='" + username + "' and password='" + password + "' ";
        List<Meter_readers.to_meter_readers> readers = Meter_readers.ret_data(where);
        if (!readers.isEmpty()) {

            Meter_readers.to_meter_readers reader = (Meter_readers.to_meter_readers) readers.get(0);
            MyUser.setUser_id("" + reader.id);
            MyUser.setUser_name(reader.user_name);
            MyUser.setUser_screen_name(reader.lname + ", " + reader.fname + " " + reader.mi);
            meter_reading(reader);
        } else {
            Alert.set(0, "Incorrect username/password!");
            jPasswordField1.setText("");
            jTextField1.grabFocus();
        }
    }

